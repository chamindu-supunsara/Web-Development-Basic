            <div class="b">The Cultural Triangle is an area in the Northern District of Sri Lanka. The places to see there are architectural remains of rock top fortresses and ancient temples. Start at either Colombo (or Kandy if you just did the mountains road trip). First destination is the ancient city of Anurhadapura where you can visit the ancient Boddhi tree or the giant dagoba. If you are lucky enough to be around on Poya day you can join the worshippers that go there to celebrate the full moon.</div>
